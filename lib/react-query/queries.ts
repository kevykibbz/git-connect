type InfinitePostsResponse = {
  documents: Models.Document;
  total: number;
};

export const useGetPosts = () => {
  return useInfiniteQuery<InfinitePostsResponse>({
    queryKey: [QUERY_KEYS.GET_INFINITE_POSTS],
    queryFn: getInfinitePosts as unknown as QueryFunction<InfinitePostsResponse, QueryKey, unknown>,
    getNextPageParam: (lastPage:InfinitePostsResponse) => {
      if (lastPage.documents.length === 0) {
        return null;
      }
      const lastId = lastPage.documents[lastPage.documents.length - 1].$id;
      return lastId;
    },
    initialPageParam: 0,
  });
};



